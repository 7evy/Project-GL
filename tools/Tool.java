public class Tool{

    /**
    * Print a String
    * @author Sébastien HERT (group 8)
    * @param String s to print
    */
    public static void print(String s){
        System.out.println(s);
    }

    /**
    * Print a Object
    * @author Sébastien HERT (group 8)
    * @param Object o to print
    */
    public static void display(Object o){
        System.out.println(o.toString());
    }

}